	WHERE FeatureLabel = 'Load in CIRCE'
	AND FEATURE_FEATURE_GROUP_IDR = (select FEATURE_GROUP_ID from T_EDIP_FEATURE_GROUP where FeatureGroupLabel = 'WDDS')

END